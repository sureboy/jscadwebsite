/**
 * modeling 补全源函数
 * @param context - CodeMirror 补全上下文
 * @returns 补全结果或 null
 */
export function modelingCompletions(context: CompletionContext): CompletionResult | null {
  const word = context.matchBefore(/[\w\.]*/);
  
  if (!word || (word.from === word.to && !context.explicit)) {
    return null;
  }

  // 检查是否在导入语句中
  const lineText = context.state.doc.lineAt(context.pos).text;
  const trimmedLine = lineText.trim();
  
  // 跳过 import 和 export 语句
  if (trimmedLine.startsWith('import') || trimmedLine.startsWith('export') || trimmedLine.includes('from')) {
    return null;
  }

  const query = word.text.toLowerCase();
  
  // 过滤和排序补全项
  const filtered = modelingCompletionData
    .filter(option => option.label.toLowerCase().includes(query))
    .sort((a, b) => {
      // 优先完全匹配
      if (a.label.toLowerCase() === query) return -1;
      if (b.label.toLowerCase() === query) return 1;
      
      // 然后按优先级排序
      return (b.boost || 0) - (a.boost || 0);
    });

  if (filtered.length === 0) {
    return null;
  }

  return {
    from: word.from,
    options: filtered.slice(0, 50), // 限制最大数量
    validFor: /^[\w\.]*$/
  };
}

// 导出为可直接使用的 CodeMirror 扩展
export const modelingCompletion = () => autocompletion({
  override: [modelingCompletions]
});

// 默认导出补全函数
export default modelingCompletions;
